package com.popova;

public class Region {
    int regionCode;
    String regionName;

    public void setRegionCode(int regionCode) {
        this.regionCode = regionCode;
    }

    public void setRegionName(String regionName) {
        this.regionName = regionName;
    }

    public int getRegionCode() {

        return regionCode;
    }

    public String getRegionName() {
        return regionName;
    }
}
